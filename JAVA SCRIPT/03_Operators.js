// Arithmetic Operators
let a = 50;
let b = 5;
console.log("a + b =", a+b);
console.log("a - b =", a-b);
console.log("a * b =", a*b);
console.log("a / b =", a/b);
console.log("a ** b =", a**b);
console.log("a % b =", a%b);
console.log("a ++ b =", a++);
console.log("a -- b =", a--);

// Assignment Operators
let x = 3;
x += 5 // same as x = x + 5
console.log("a is now = ", x);
x -= 5 // same as x = x - 5
console.log("a is now = ", x);
x *= 5 // same as x = x * 5
console.log("a is now = ", x);
x /= 5 // same as x = x / 5
console.log("a is now = ", x);

// Comparision operators
let comp1 = 6;
let comp2 = 7;
console.log("comp1 == comp2 is", comp1 == comp2);
console.log("comp1 != comp2 is", comp1 != comp2);
console.log("comp1 === comp2 is", comp1 === comp2);
console.log("comp1 !== comp2 is", comp1 !== comp2);
console.log("comp1 > comp2 is", comp1 > comp2);
console.log("comp1 < comp2 is", comp1 < comp2);

