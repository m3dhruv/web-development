const age = 10;

switch (age) {
  case 10:
    console.log("your age is 10");
    break;

  case 20:
    console.log("your age is 20");
    break;

  case 30:
    console.log("your age is 30");
    break;

  case 40:
    console.log("your age is 40");
    break;

  case 50:
    console.log("your age is 50");
    break;

  default:
    console.log("your age is not special");
    break;
}
